# integer data type
x = 10
print("The value of x is:", x)
print("The data type of x is:", type(x))

# float data type
y = 3.14
print("The value of y is:", y)
print("The data type of y is:", type(y))

# complex data type
z = 2 + 3j
print("The value of z is:", z)
print("The data type of z is:", type(z))

