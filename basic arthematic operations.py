# addition
a = 10
b = 5
c = a + b
print("Addition of", a, "and", b, "is:", c)

# subtraction
d = 20
e = 8
f = d - e
print("Subtraction of", d, "and", e, "is:", f)

# multiplication
g = 4
h = 6
i = g * h
print("Multiplication of", g, "and", h, "is:", i)

# division
j = 25
k = 5
l = j / k
print("Division of", j, "and", k, "is:", l)

# modulus
m = 30
n = 7
o = m % n
print("Modulus of", m, "and", n, "is:", o)

# exponentiation
p = 2
q = 3
r = p ** q
print(p, "raised to the power of", q, "is:", r)
